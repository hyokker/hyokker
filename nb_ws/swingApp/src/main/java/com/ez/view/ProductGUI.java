    public void actionPerformed(ActionEvent e) {
        if(e.getSource()==btAdd){
            try {
                addData();
            } catch (SQLException ex) {
                Logger.getLogger(ProductGUI.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else if(e.getSource()==btSearch){
            search();
        }
    }

    private void addData() throws SQLException {
        //1
        String pdName=tfPdName.getText();
        String price=tfPrice.getText();
        String desc=taDesc.getText();
        
        if(pdName==null||pdName.isEmpty()){
            JOptionPane.showMessageDialog(this, "상품명 입력");
            tfPdName.requestFocus();
            return;
        }
        //2
        PdDTO dto = new PdDTO();
        dto.setPdName(pdName);
        dto.setPrice(Integer.parseInt(price));
        dto.setDescription(desc);
        int cnt=pdDao.insertPd(dto);
        //3
        String msg="";
        if(cnt>0){
            msg="등록 성공";
            clear_tf();
            showAllData();
        }else{
            msg="등록 실패";
        }
        JOptionPane.showMessageDialog(this, msg);
        }


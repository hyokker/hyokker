    private void showAllData() throws SQLException {
        //1        
        //2
        List<PdDTO> list=pdDao.selectAll();
        if(list==null || list.isEmpty()){
            JOptionPane.showMessageDialog(this, "데이터가 없습니다.");
            return;
        }
        
        //3
        String[][] data=new String[list.size()][colNames.length];
        DecimalFormat df = new DecimalFormat("#,###");
        
        for(int i=0;i<list.size();i++){
            PdDTO dto=list.get(i);
            
            data[i][0]=dto.getNo()+"";
            data[i][1]=dto.getPdName();
            data[i][2]=df.format(dto.getPrice());            
        }//for
        
        model.setDataVector(data, colNames);
        table.setModel(model);
        
        //각 컬럼 사이즈 조절
        table.getColumnModel().getColumn(0).setPreferredWidth(40);
        table.getColumnModel().getColumn(1).setPreferredWidth(120);
        table.getColumnModel().getColumn(2).setPreferredWidth(60);
                
        //가격 오른쪽 정렬
        DefaultTableCellRenderer dtcr = new DefaultTableCellRenderer();
        dtcr.setHorizontalAlignment(SwingConstants.RIGHT);
        table.getColumnModel().getColumn(2).setCellRenderer(dtcr);
    }

